--자동 형변환
SELECT * FROM EMPLOYEES;
SELECT * FROM EMPLOYEES WHERE SALARY >= '1000'; -- 문자 -> 숫자 자동형변환
SELECT * FROM EMPLOYEES WHERE HIRE_DATE >= '05/01/01' ; --문자 -> 날짜 자동형변환

--강제형변환
--TO_CHAR -> 날짜 , 문자로 강제 형변환 , 날짜 포멧형식이 쓰인다
SELECT TO_CHAR(SYSDATE) FROM DUAL;
SELECT TO_CHAR(SYSDATE, 'YYYY-MM-DD HH:MI:SS') AS TIME FROM DUAL;
SELECT TO_CHAR(SYSDATE, 'YY-MM-DD') AS TIME FROM DUAL;
SELECT TO_CHAR(SYSDATE, 'YY-MM-DD AM') AS TIME FROM DUAL;

SELECT TO_CHAR(SYSDATE, 'YYYY"년" MM"월" DD"일"') FROM DUAL; --데이트 포맷형식이 아닌 값을 쓰려면 ""로 묶어준다

SELECT TO_CHAR(HIRE_DATE, 'YYYY-MM-DD') FROM EMPLOYEES;

--TO_CHAR -> 숫자, 문자로 강제 형변환, 숫자 포맷형식이 쓰인다(9 0, . S L)
SELECT TO_CHAR(20000, '9999999999'), 20000 FROM DUAL; -- 문자 10자리로 변경
SELECT TO_CHAR(20000, '09999999999'), 20000 FROM DUAL; -- 문자 10자리로 변경
SELECT TO_CHAR(20000, '9999'),20000 FROM DUAL; --자리수가 부족하면 #으로 처리 된다
SELECT TO_CHAR(20000.123, '99999.999')FROM DUAL; -- 소수점은 .으로 나타낸다,는 그냥 쓰면된다
SELECT TO_CHAR(20000, '$999999') AS A FROM DUAL; --$도 그냥 쓰면 됩니다.
SELECT TO_CHAR(20000, 'L999999') AS A FROM DUAL; --L도 지역화폐 기호

--오늘의 환율이 1302.69원 입니다
-- SALARY컬럼을 한국돈으로 변경해서 소수점 2자리수까지 출력
SELECT FIRST_NAME,TO_CHAR(SALARY*1302.69,'L99999999.00') AS A FROM EMPLOYEES;

--TO_NUMBER ->문자, 숫자로 강제 형변환
SELECT '2000' + 2000 FROM DUAL; -- 명시적형변환
SELECT TO_NUMBER('$5,000', '$9,999')+ 2000 FROM DUAL; --숫자로 변환이 가능한 문자일 경우,숫자 fmt을 이용해서 자리를 맞추면됨

--TO_DATE -> 문자,날짜로 강제 형변환
SELECT TO_DATE('2023-12-04') FROM DUAL; --날짜로 형변환

SELECT SYSDATE - '2023-12-03' FROM DUAL; --날짜로 변환해야 일자가 나온다.

SELECT TO_DATE('2023/12/04', 'YYYY/MM/DD') FROM DUAL;
SELECT TO_DATE('2023년12월04일') FROM DUAL; --포맷없으면 안됨
SELECT TO_DATE('2023년12월04일', 'YYYY"년"MM"월"DD"일"') FROM DUAL;
SELECT '2024-12-04 02:30:23' FROM DUAL;
SELECT TO_DATE('2024-12-04 02:30:23' , 'YYYY-MM-DD HH:MI:SS') FROM DUAL; --자리에 맞춰서 포멧형식을 적어준다

--XXXX년 XX월XX일 로 변환
SELECT TO_CHAR(TO_DATE('20050102','YYYY-MM-DD'),'YYYY"년" MM"월" DD"일"') FROM DUAL;

